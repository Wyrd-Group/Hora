export interface AgentAbility {
  name: string;
  description: string;
  cooldownTicks: number;        // Ticks between uses
  effect: {
    type: 'income_boost' | 'cost_reduction' | 'research_speed' | 'trade_edge'
      | 'detection_reduction' | 'network_bonus' | 'event_prediction' | 'supply_chain'
      | 'venture_boost' | 'political_influence' | 'crisis_profit' | 'innovation_speed'
      | 'hack_defense' | 'data_harvest' | 'market_intel' | 'recruitment'
      | 'automation' | 'diplomacy' | 'sabotage' | 'healing';
    value: number;              // Multiplier or flat bonus
    duration: number;           // Ticks the effect lasts
    target: 'self' | 'node' | 'department' | 'empire' | 'rival' | 'region';
  };
}

export interface AgentCardDef {
  id: string;
  name: string;                 // Display name (inspired by real project)
  codename: string;             // The real open-source project name
  class: AgentClass;
  rarity: AgentRarity;
  // Stats (1-100 scale)
  stats: {
    intelligence: number;       // Problem-solving capability
    speed: number;              // Execution speed
    stealth: number;            // Undetectability
    loyalty: number;            // Reliability / won't go rogue
    adaptability: number;       // Handles unexpected situations
    influence: number;          // Social/political power
  };
  // Passive buff (always active when deployed)
  passive: {
    type: string;
    value: number;
    description: string;
  };
  // Active ability (triggered manually or by AI)
  ability: AgentAbility;
  // Ultimate ability (unlocked at level 5)
  ultimate?: AgentAbility;
  // Visual / NFT
  portraitGradient: [string, string, string]; // 3-color gradient for card art
  iconGlyph: string;            // Unicode/emoji for the agent
  edition: 'Genesis' | 'Alpha' | 'Beta' | 'Standard' | 'Swarm';
  maxSupply: number;            // Max copies that can exist
  lore: string;                 // Flavor text
  // NFT Identity
  biography: {
    fullName: string;           // Full character name
    title: string;              // Professional title
    origin: string;             // Where they're from
    age: number;                // Age
    background: string;         // 2-3 sentence backstory
    specialization: string;     // What they're best at
    weakness: string;           // Their flaw
    quote: string;              // Signature quote
  };
  portrait: {
    style: 'gradient' | 'photo' | 'pixel' | 'anime';
    seed: number;               // Deterministic seed for generation
    expression: string;         // e.g. 'determined', 'mysterious', 'cunning'
    attire: string;             // e.g. 'tactical suit', 'lab coat', 'hoodie'
  };
  // NFT Metadata (ERC-721 compatible)
  nft: {
    tokenStandard: 'ERC-721';
    collection: string;         // 'AEGIS Agents Genesis'
    contractSymbol: string;     // 'AEGIS-AGT'
    attributes: Array<{ trait_type: string; value: string | number }>;
    externalUrl: string;
  };
  // Synergy
  synergyTags: string[];        // Tags for combo bonuses
  synergyBonus?: string;        // Description of synergy effect
}

