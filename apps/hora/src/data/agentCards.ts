export function computeOverallRating(
  stats: { intelligence: number; speed: number; stealth: number; loyalty: number; adaptability: number; influence: number },
  agentClass: AgentClass,
): number {
  const weights = OVR_WEIGHTS[agentClass];
  let ovr = 0;
  for (const [stat, weight] of Object.entries(weights)) {
    ovr += (stats[stat as keyof typeof stats] ?? 0) * weight;
  }
  return Math.min(99, Math.max(1, Math.round(ovr)));
}

