// ── 500 Agent Catalog (merged) ───────────────────────────────────
// Import the expanded 500-agent catalog and merge with originals.
// The generator creates 424 new agents; combined with the 76 originals = 500 total.
import { AGENT_CATALOG_500 } from './agentCards500';

// Build a merged catalog: originals first, then generated agents that don't overlap
const originalIds = new Set(AGENT_CATALOG.map(a => a.id));
const extraAgents = AGENT_CATALOG_500.filter(a => !originalIds.has(a.id));
const MERGED_CATALOG: AgentCardDef[] = [...AGENT_CATALOG, ...extraAgents];

// ── Helpers (use merged 500-agent catalog) ───────────────────────

export function getAgentById(id: string): AgentCardDef | undefined {
  return MERGED_CATALOG.find(a => a.id === id);
}

export function getAgentsByClass(cls: AgentClass): AgentCardDef[] {
  return MERGED_CATALOG.filter(a => a.class === cls);
}

export function getAgentsByRarity(rarity: AgentRarity): AgentCardDef[] {
  return MERGED_CATALOG.filter(a => a.rarity === rarity);
}

export function getSynergyPartners(agent: AgentCardDef): AgentCardDef[] {
  return MERGED_CATALOG.filter(other =>
    other.id !== agent.id &&
    other.synergyTags.some(tag => agent.synergyTags.includes(tag))
  );
}

/** Full 500-agent catalog for pack opening and gallery */
export function getAllAgents(): AgentCardDef[] {
  return MERGED_CATALOG;
}

/** Generate a unique mint ID for an agent card instance */
export function generateMintId(agentId: string, edition: number): string {
  const ts = Date.now().toString(36);
  const rand = Math.random().toString(36).slice(2, 6);
  return `${agentId}-${edition.toString().padStart(4, '0')}-${ts}${rand}`;
}
