// ── Types ──────────────────────────────────────────────────────────

export type PricePattern =
  | 'uptrend'
  | 'downtrend'
  | 'sideways'
  | 'volatile'
  | 'crash'
  | 'recovery'
  | 'bubble'
  | 'mean-revert'
  | 'breakout'
  | 'whipsaw';

export type Difficulty = 'beginner' | 'intermediate' | 'advanced';

export interface LabPriceBehavior {
  pattern: PricePattern;
  volatility: number;   // 0.5 = low, 1 = normal, 2 = high, 3+ = extreme
  trendStrength: number; // 0 = no trend, 0.5 = moderate, 1 = strong
  seed: number;          // unique seed for deterministic generation
}

export interface LabAnnotation {
  tick: number;
  label: string;
}

export interface LabScenario {
  id: string;
  title: string;
  description: string;
  year: string;
  difficulty: Difficulty;
  duration: number;
  instruments: string[];
  annotations: LabAnnotation[];
  category: string;
  objective: string;
  teaches: string[];
  startingBalance: number;
  priceBehavior: LabPriceBehavior;
}

export interface LabCategory {
  id: string;
  label: string;
  description: string;
  icon: string;
}

