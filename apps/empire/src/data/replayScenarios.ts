function convertLabToReplay(lab: LabScenario): ReplayScenario {
  const cached = labScenarioCache.get(lab.id);
  if (cached) return cached;

  const { segments, startPrice } = labPriceBehaviorToSegments(lab.priceBehavior, lab.duration);
  const mainInstrument = lab.instruments[0];
  const prices = buildPriceCurve(startPrice, segments, lab.duration, lab.priceBehavior.seed);

  const scenario: ReplayScenario = {
    id: lab.id,
    title: lab.title,
    description: lab.description,
    year: lab.year,
    difficulty: lab.difficulty,
    duration: lab.duration,
    instruments: lab.instruments,
    priceData: { [mainInstrument]: prices },
    annotations: lab.annotations.map(a => ({ tick: a.tick, text: a.label, type: 'info' as const })),
    startingBalance: lab.startingBalance,
    starThresholds: lab.difficulty === 'beginner' ? [5, 15, 30]
      : lab.difficulty === 'intermediate' ? [3, 10, 25]
      : [0, 5, 15],
  };

  labScenarioCache.set(lab.id, scenario);
  return scenario;
}

export function getScenarioById(id: string): ReplayScenario | undefined {
  if (id === '__free_sim__') return FREE_SIM_SCENARIO;
  const replay = REPLAY_SCENARIOS.find((s) => s.id === id);
  if (replay) return replay;
  const lab = LAB_SCENARIOS.find((s) => s.id === id);
  if (lab) return convertLabToReplay(lab);
  return undefined;
}
