// ── Build final scenarios with pre-computed price data ──────────────

function buildAnnotations(raw: RawScenario): ReplayAnnotation[] {
  const annotations: ReplayAnnotation[] = [];

  for (const ev of raw.events) {
    annotations.push({
      tick: ev.tick,
      text: `${ev.title}: ${ev.description}`,
      type: 'event',
    });
  }

  for (const p of raw.patterns) {
    annotations.push({
      tick: p.tick,
      text: `[${p.pattern}] ${p.hint}`,
      type: 'info',
    });
  }

  return annotations.sort((a, b) => a.tick - b.tick);
}

function inferStarThresholds(raw: RawScenario): [number, number, number] {
  // Derive from the scenario difficulty and price movement
  const lastFactor = raw.segments[raw.segments.length - 1].endPriceFactor;
  if (raw.difficulty === 'beginner') {
    return [5, 15, 30]; // 5%, 15%, 30% return
  }
  if (raw.difficulty === 'intermediate') {
    return [3, 10, 25];
  }
  // advanced
  if (lastFactor < 0.8) {
    // crash scenarios -- positive return is great
    return [0, 5, 15];
  }
  return [5, 15, 35];
}

export const REPLAY_SCENARIOS: ReplayScenario[] = RAW_SCENARIOS.map((raw) => {
  const instrumentId = raw.asset.toLowerCase();
  const priceArray = buildPriceCurve(raw.startPrice, raw.segments, raw.totalTicks, raw.seed);

  return {
    id: raw.id,
    title: raw.title,
    description: raw.description,
    year: raw.period,
    difficulty: raw.difficulty,
    duration: raw.totalTicks,
    instruments: [instrumentId],
    priceData: { [instrumentId]: priceArray },
    annotations: buildAnnotations(raw),
    startingBalance: 10000,
    starThresholds: raw.starThresholds ?? inferStarThresholds(raw),
    era: raw.era,
    historicalReturn: raw.historicalReturn,
  };
});

