// ── Deterministic PRNG (matches MVP seededRandom) ──────────────────

function seededRandom(seed: number): number {
  const x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
}

// ── Segment-based price curve builder ──────────────────────────────

interface PriceSegment {
  endTick: number;
  endPriceFactor: number;
  volatility: number;
}

function buildPriceCurve(
  startPrice: number,
  segments: PriceSegment[],
  totalTicks: number,
  seed: number,
): number[] {
  const prices: number[] = [startPrice];
  let price = startPrice;
  let segIdx = 0;
  let segStart = 0;
  let segStartPrice = startPrice;

  for (let t = 1; t < totalTicks; t++) {
    while (segIdx < segments.length - 1 && t > segments[segIdx].endTick) {
      segIdx++;
      segStart = segments[segIdx - 1].endTick;
      segStartPrice = prices[segStart] ?? price;
    }

    const seg = segments[segIdx];
    const segLen = seg.endTick - segStart;
    const progress = segLen > 0 ? (t - segStart) / segLen : 1;
    const targetPrice = segStartPrice * seg.endPriceFactor;
    const trendPrice = segStartPrice + (targetPrice - segStartPrice) * progress;
    const noise = (seededRandom(seed + t) - 0.5) * 2 * seg.volatility * trendPrice;
    price = Math.max(trendPrice * 0.1, trendPrice + noise);
    prices.push(Number(price.toFixed(2)));
  }
  return prices;
}

